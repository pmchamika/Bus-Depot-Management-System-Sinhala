    public String Validateticket_part() {

        String ssp = sp.getText();
        String sqn = qn.getText();
        String sps = ps.getText();

        if (ssp.equals("")) {
            a1.setText("fuu ;Srej ysiaj ;eìh fkdyelsh");
            return "fail";

        } else {
            a1.setText("");

        }
        if (sqn.equals("")) {
            a2.setText("fuu ;Srej ysiaj ;eìh fkdyelsh");
            return "fail";

        } else {
            regexPattern = Pattern.compile("^[0-9]{1,3}");
            regMatcher = regexPattern.matcher(sqn);
            if (regMatcher.matches()) {
                a2.setText("");
            } else {
                a2.setText("m%ukh jeros  ");
                return "fail";
            }

        }
        if (sps.equals("")) {
            a3.setText("fuu ;Srej ysiaj ;eìh fkdyelsh");
            return "fail";

        } else {
            regexPattern = Pattern.compile("^[0-9]{1,10}");
            regMatcher = regexPattern.matcher(sps);
            if (regMatcher.matches()) {
                a3.setText("");
            } else {
                a3.setText(" ñ, jeros  ");
                return "fail";
            }

        }

        return "success";

    }

