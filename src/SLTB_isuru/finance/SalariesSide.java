    private void paysheetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_paysheetActionPerformed
        // TODO add your handling code here:
        Finance f1 = new Finance();
        Salaries fe = Salaries.getobj();
        SalariesUp fEup = SalariesUp.getobj();
        String se = fEup.search.getText();

        String rPath = "src\\SLTB_isuru\\report\\PaySheet1.jasper";

        HashMap par = new HashMap();

        par.put("sid", se);

        if (paysheet.getText().equalsIgnoreCase("PaySheet")) {
            paysheet.setBackground(new java.awt.Color(255, 51, 51));
            paysheet.setText("Cancel");

            fEup.setVisible(false);
            fe.setVisible(false);
            report.setVisible(false);
            f1.ParameterizedReport(rPath, par);

        } else if (paysheet.getText().equalsIgnoreCase("Cancel")) {
            paysheet.setBackground(new java.awt.Color(102, 153, 255));
            paysheet.setText("PaySheet");

            f1.viewer.setVisible(false);
            fEup.setVisible(true);
            fe.setVisible(true);
            report.setVisible(true);
            //dailyReport.setVisible(true);
        }
    }//GEN-LAST:event_paysheetActionPerformed

